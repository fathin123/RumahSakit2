    public final void tampilkanData(){
       String[] kolom = {"Id_Obat", "Nama Obat", "Jenis Obat", "Harga Obat"};
         ArrayList<Obat> list = new Obat().getAll();
        Object rowData[] = new Object[4];
        
        tblObat.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Obat ob : list) {
            rowData[0] = ob.getIdobat();
            rowData[1] = ob.getNama_obat();
            rowData[2] = ob.getJenis_obat();
            rowData[3] = ob.getHarga_obat();
        
            ((DefaultTableModel)tblObat.getModel()).addRow(rowData);
        }
    }
    
    public final void cari(String keyword){
       String[] kolom = {"Id_obat", "Nama Obat", "Jenis Obat", "Harga Obat"};
        ArrayList<Obat> list = new Obat().search(keyword);
        Object rowData[] = new Object[4];
        
        tblObat.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Obat ob : list) {
            rowData[0] = ob.getIdobat();
            rowData[1] = ob.getNama_obat();
            rowData[2] = ob.getJenis_obat();
            rowData[3] = ob.getHarga_obat();
        
            ((DefaultTableModel)tblObat.getModel()).addRow(rowData);
        }
    }

