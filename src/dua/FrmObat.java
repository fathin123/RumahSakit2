    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Obat ob = new Obat();
        ob.setIdobat(Integer.parseInt(txtIdobat.getText()));
        ob.setNama_obat(txtNama_obat.getText());
        ob.setJenis_obat(txtJenis_obat.getText());
        ob.setHarga_obat(txtHarga_obat.getText());
        ob.save();
        
        txtIdobat.setText(Integer.toString(ob.getIdobat()));
        tampilkanData();  
    }//GEN-LAST:event_btnSimpanActionPerformed

