    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Pasien pas = new Pasien();
        pas.setIdPasien(Integer.parseInt(txtIdPasien.getText()));
        pas.setIdDokter(txtIdDokter.getText());
        pas.setNama_pasien(txtNamaPasien.getText());
        pas.setPenyakit(txtPenyakit.getText());
        pas.setJk(txtJk.getText());
        pas.setAlamat(txtAlamat.getText());
        pas.setTelp(txtTelp.getText());
        pas.save();
        
        txtIdPasien.setText(Integer.toString(pas.getIdPasien()));
        tampilkanData(); 
    }//GEN-LAST:event_btnSimpanActionPerformed

