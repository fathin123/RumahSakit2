        int row = tblPasien.getSelectedRow();
        
        txtIdPasien.setText(model.getValueAt(row, 0).toString());
        txtIdDokter.setText(model.getValueAt(row, 1).toString());
        txtNamaPasien.setText(model.getValueAt(row, 2).toString());
