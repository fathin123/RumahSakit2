    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(tblPasien.getSelectionModel().isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus!");
            kosongkanForm();
        }else {
        DefaultTableModel model = (DefaultTableModel)tblPasien.getModel();
        int row = tblPasien.getSelectedRow();
            
        Pasien pas = new Pasien().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        pas.delete();
        kosongkanForm();
        tampilkanData();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

