    public final void tampilkanData(){
        String[] kolom = {"ID Pasien","ID Dokter", "Nama Pasien", "Penyakit", "Jenis Kelamin", "ALamat", "Telepon"};
        ArrayList<Pasien> list = new Pasien().getAll();
        Object rowData[] = new Object[7];
        
        tblPasien.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Pasien pas : list) {
            rowData[0] = pas.getIdPasien();
            rowData[1] = pas.getIdDokter();
            rowData[2] = pas.getNama_pasien();
            rowData[3] = pas.getPenyakit();
            rowData[4] = pas.getJk();
            rowData[5] = pas.getAlamat();
            rowData[6] = pas.getTelp();
        
            ((DefaultTableModel)tblPasien.getModel()).addRow(rowData);
        }
    }
    
    public final void cari(String keyword){
        String[] kolom = {"ID Pasien","ID Dokter", "Nama Pasien", "Penyakit", "Jenis Kelamin", "ALamat", "Telepon"};
        ArrayList<Pasien> list = new Pasien().search(keyword);
        Object rowData[] = new Object[7];
        
        tblPasien.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Pasien pas : list) {
            rowData[0] = pas.getIdPasien();
            rowData[1] = pas.getIdDokter();
            rowData[2] = pas.getNama_pasien();
            rowData[3] = pas.getPenyakit();
            rowData[4] = pas.getJk();
            rowData[5] = pas.getAlamat();
            rowData[6] = pas.getTelp();
        
            ((DefaultTableModel)tblPasien.getModel()).addRow(rowData);
        }
    }

