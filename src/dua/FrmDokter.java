    public final void tampilkanData(){
        String[] kolom = {"ID Dokter", "Nama Dokter", "Spesialis"};
        ArrayList<Dokter> list = new Dokter().getAll();
        Object rowData[] = new Object[4];
        
        tblDokter.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Dokter ob : list) {
            rowData[0] = ob.getIdDokter();
            rowData[1] = ob.getNama_dokter();
            rowData[2] = ob.getSpesialis();
        
            ((DefaultTableModel)tblDokter.getModel()).addRow(rowData);
        }
    }
    
    public final void cari(String keyword){
        String[] kolom = {"ID Dokter", "Nama Dokter", "Spesialis"};
        ArrayList<Dokter> list = new Dokter().search(keyword);
        Object rowData[] = new Object[4];
        
        tblDokter.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Dokter ob : list) {
            rowData[0] = ob.getIdDokter();
            rowData[1] = ob.getNama_dokter();
            rowData[2] = ob.getSpesialis();
        
            ((DefaultTableModel)tblDokter.getModel()).addRow(rowData);
        }
    }

