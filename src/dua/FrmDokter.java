    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Dokter dok = new Dokter();
        dok.setIdDokter(Integer.parseInt(txtIdDokter.getText()));
        dok.setNama_dokter(txtNama_dokter.getText());
        dok.setSpesialis(txtSpesialis.getText());
        dok.save();
        
        txtIdDokter.setText(Integer.toString(dok.getIdDokter()));
        tampilkanData(); 
    }//GEN-LAST:event_btnSimpanActionPerformed

